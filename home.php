            <h3 class="profile-name">Όλγα Ποζέλη</h3>

                  <p>Γεννήθηκε  στη Θεσσαλονίκη. Σπούδασε <strong>Αγγλική  Φιλολογία</strong> στο <strong>Α. Π. Θ.</strong> και Θέατρο στην Δραματική Σχολή της Ρούλας Πατεράκη. Έκανε μεταπτυχιακές  σπουδές στο θέατρο (<strong>ΜΑ</strong>), στο <strong>Royal Holloway and Bedford New College</strong> του Λονδίνου, με υποτροφίες του Βρετανικού  Συμβουλίου και της Ακαδημίας Αθηνών.</p>

<p>Για  την Ομάδα Θεάτρου <span class="noitigrammi">ΝΟΗΤΗ ΓΡΑΜΜΗ</span>, της  οποίας είναι ιδρυτικό μέλος, σκηνοθέτησε τα έργα: <strong>Η μεγάλη της ευκαιρία</strong> του <em>Α. Bennett</em>, <strong>Ένα κήρυγμα</strong> του <em>D. Mamet</em>, <strong> Do you love me?</strong>, μια παράσταση βασισμένη στη  ζωή και τα έργα του διάσημου αντιψυχιάτρου <em>R. D. Laing</em>, <strong>Κβετς</strong> του <em>Steven Berkoff</em>, <strong>Το Πουλί της Ηδονής</strong>, μία  παράσταση devised theatre βασισμένη στο ομώνυμο διήγημα του <em>Peter Carey</em>, <strong>Όλα  όσα έχω ξεχάσει μπορούν να γεμίσουν ένα σπίτι;</strong>, devised, βασισμένη στην εικαστική εγκατάσταση των <em>Peter Fischli  & David Weiss</em> με τίτλο <em>Questions</em>, <strong>Ποτέ  μην πας απ΄τις σκάλες</strong>, και πάλι devised με θέμα τις συμπτώσεις, <strong>Μην κρίνεις έναν άνθρωπο από την ουρά του</strong>, peformance εμπνευσμένη από την εικαστική εγκατάσταση του <em>Ilya Kabakov</em> με τίτλο <em>10 Albums</em>, <strong>Όταν το κόκκινο Toyota ξέφυγε απ' τον δρόμο και βυθίστηκε στα μαύρα νερά/When the red Toyota went off the road and sank in black water</strong>, δίγλωσσο μονόδραμα βασισμένο σε μια πραγματική ιστορία, <strong>I remember/Θυμάμαι</strong>, διαδραστική performance/εγκατάσταση -επίσης δίγλωσση- εμπνευσμένη από το ομώνυμο βιβλίο του <em>Joe Brainard</em>.</p>

<p>Για το <strong>Do you love me?</strong> ήταν υποψήφια για το βραβείο Νέου Δημιουργού της <strong>Ένωσης  Θεατρικών και Μουσικών Κριτικών</strong>. Το <strong>Do you love me?</strong> ήταν επίσης υποψήφιο για δύο βραβεία του περιοδικού <strong>«ΑΘΗΝΟΡΑΜΑ»</strong>. Το <strong>Κβετς </strong>ήταν υποψήφιο για έξι βραβεία του  περιοδικού <strong>«ΑΘΗΝΟΡΑΜΑ»</strong>, μεταξύ των οποίων και αυτά της καλύτερης  παράστασης και σκηνοθεσίας. Τον Ιούλιο του 2012  η Επιτροπή των Σκηνοθετών του <strong>International Festival of Monoperformances <em>Vidlunnja</em></strong> της απένειμε <strong>Βραβείο</strong>  (Festival Directorsʼ Award) για την παράσταση <strong>Όταν το κόκκινο Toyota ξέφυγε απ' τον δρόμο και βυθίστηκε στα μαύρα νερά/When the red Toyota went off the road and sank in black water</strong>. Τον Νοέμβριο του 2016, απέσπασε ένα ακόμα βραβείο, το <strong>Βραβείο Πρωτοποριακής Παράστασης</strong> (Award for the Most Innovative Show), στο <strong>3<sup>ο</sup> Διεθνές Φεστιβάλ Πειραματικού Θεάτρου του <em>Ανόι</em></strong>, για την performance <strong>Θυμάμαι/I remember</strong>.</p>

<p>Στο <strong>Φεστιβάλ Μπέκετ</strong> στο  θέατρο <strong>Σφενδόνη </strong>σκηνοθέτησε το μονόπρακτο <strong>Θέατρο</strong>. Στο Θεάτρο <strong>Επί Κολωνώ</strong> σκηνοθέτησε 4 μονόπρακτα του <em>David Ives</em>. με  γενικό τίτλο <strong>8 Παραλλαγές στον Θάνατο του Τρότσκι</strong>.</p>

<p>Για  το <strong>ΔΗ.ΠΕ.ΘΕ. Βέροιας</strong> σκηνοθέτησε τον <strong>Μάγο  του Οζ</strong> και τους <strong>Όρνιθες</strong> σε διασκευή του Γ.  Καλατζόπουλου.</p>

<p>Ως  ηθοποιός έλαβε μέρος σε παραστάσεις του Εθνικού Θεάτρου και του ελεύθερου, στην  μεγάλου μήκους ταινία <strong>Έναστρος Θόλος</strong> του Κ. Αριστόπουλου και στις τηλεοπτικές σειρές: <strong>Στον Αργαλειό του Φεγγαριού</strong> (σκην. Κ. Ζώης) και <strong>Αττίκ</strong> (σκην. Κ. Αριστόπουλος).</p>

