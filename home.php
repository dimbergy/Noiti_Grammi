            <h3 class="profile-name">Η ιστορία μας</h3>

             <p>H Ομάδα Θεάτρου<span class="noitigrammi"> ΝΟΗΤΗ ΓΡΑΜΜΗ</span> ιδρύθηκε το 1999 από την Όλγα Ποζέλη. Εναρκτήρια παράσταση της  ομάδας ήταν <strong>Η μεγάλη της ευκαιρία</strong> του <em>Alan Bennett</em> και το <strong>Ένα κήρυγμα</strong> του <em>David Mamet</em>, στον <strong>Τεχνοχώρο Υπό Σκιάν</strong>.</p>

             <p>Ακολούθησε το  <strong>Dο you love me?</strong>, μια παράσταση devised theatre βασισμένη στη ζωή και τα κείμενα του ψυχίατρου <strong>R. D. Laing</strong>, αρχικά στον <strong>Άδειο  Χώρο της Πειραματικής Σκηνής</strong> του <strong>Εθνικού Θεάτρου</strong> (Ιούνιος 2001) και  κατόπιν στο Θέατρο <strong>ΦΟΥΡΝΟΣ</strong> (Οκτώβριος - Δεκέμβριος 2001), στο Δημοτικό  Θέατρο <strong>ΑΝΕΤΟΝ</strong> της Θεσσαλονίκης  (Φεβρουάριος 2002) και στο <strong>Φεστιβάλ  Πειραματικού Θεάτρου της Τρίπολης</strong> (Ιούλιος 2002). Το <strong>Do you love me?</strong> ήταν υποψήφιο για δύο βραβεία του περιοδικού <strong>«ΑΘΗΝΟΡΑΜΑ».</strong> Για το <strong>Do you love me? </strong>η Όλγα Ποζέλη ήταν υποψήφια για το βραβείο Νέου Δημιουργού της Ένωσης  Θεατρικών και Μουσικών Κριτικών.</p>

             <p>Το Μάρτιο του 2003 η <span class="noitigrammi">ΝΟΗΤΗ ΓΡΑΜΜΗ</span> ανέβασε στο Θέατρο <strong>ΕΜΠΡΟΣ</strong> το έργο του <em>Steven Berkoff</em> <strong>Kβετς</strong>. Το <strong>Κβετς </strong>ήταν υποψήφιο για  έξι βραβεία του περιοδικού <strong>«ΑΘΗΝΟΡΑΜΑ», </strong>μεταξύ των οποίων και αυτά της  καλύτερης παράστασης και σκηνοθεσίας.</p>

             <p>Τον Απρίλιο του 2004, η<span class="noitigrammi"> ΝΟΗΤΗ ΓΡΑΜΜΗ</span> ανέβασε στο θέατρο <strong>ΕΠΙ  ΚΟΛΩΝΩ</strong> το έργο <strong>Το πουλί της ηδονής</strong>, μια παράσταση devised εμπνευσμένη από το διήγημα του βραβευμένου Αυστραλού συγγραφέα <em>Peter Carey</em> <strong>Exotic Pleasures</strong>.</p>

            <p>Τον Μάιο του  2007, η<span class="noitigrammi"> ΝΟΗΤΗ ΓΡΑΜΜΗ</span> ανέβασε στο  θέατρο <strong>ΑΡΓΩ Studio</strong> την παράσταση devised <strong>Όλα όσα έχω ξεχάσει μπορούν να γεμίσουν ένα σπίτι;</strong>, εμπνευσμένη από μια εικαστική εγκατάσταση των <em>Peter Fischli & David Weiss</em> και τον Δεκέμβρη του 2008 το επίσης devised <strong>Ποτέ μην πας απ'  τις σκάλες, </strong>με αφορμή διηγήματα του <em>Haruki Murakami</em>. Ακολούθησε το <strong>Μην κρίνεις έναν άνθρωπο από την ουρά του</strong>, μια peformance εμπνευσμένη από την εικαστική εγκατάσταση του <em>Ilya Kabakov</em> με τίτλο <strong>10 Albums</strong>, που παρουσιάστηκε στο Θέατρο <strong>ΦΟΥΡΝΟΣ</strong> (2010-2011) και κατόπιν στο <strong>Fadjr International Theatre Festival</strong> (Τεχεράνη, Ιράν, 14-15/2/2011).</p>

            <p>Το 2012-13 η <span class="noitigrammi">ΝΟΗΤΗ ΓΡΑΜΜΗ</span> ασχολείται για πρώτη φορά με το μονόδραμα, ανεβάζοντας τη δίγλωσση παράσταση (ελληνικά & αγγλικά) <strong>Όταν το κόκκινο Toyota ξέφυγε απ’ τον δρόμο και βυθίστηκε στα μαύρα νερά | When the red Toyota went off the road and sank in black water</strong>, βασισμένη σε μια πραγματική ιστορία. Η παράσταση ταξίδεψε σε πλήθος διεθνών φεστιβάλ μονοδράματος: <strong>Fundamental Monodrama Festival</strong> (Luxembourg, 27/6-7/7/2012), <strong>Global Forum of the Arts – To the Stars</strong> (Varna, Bulgaria, 1-7/8/2012), <strong>Between the Seas Festival of Mediterranean Performing Arts</strong> (New York, USA, 20-26/8/2012), <strong>Atspindys International Festival of Monoperformances</strong> (Visaginas, Lithuania, 10-13/10/2012), <strong>Thespis International Monodrama Festival</strong> (Kiel, Germany, 9-16/9/2012) και <strong>International Festival of Monoperformances <em>Vidlunnja</em></strong> (Kiev, Ukraine, 12-17/7/2013), όπου απέσπασε το <strong>Βραβείο των Σκηνοθετών του Φεστιβάλ</strong> (Festival Directors’ Award). Τέλος, ανέβηκε και στην Αθήνα, στο Θέατρο <strong>ΦΟΥΡΝΟΣ</strong> μεταξύ Δεκεμβρίου 2012 και Φεβρουαρίου 2013.</p>

            <p>Τον Ιούνιο του 2014 και σε συμπαραγωγή με το <strong>Fundamental Monodrama Festival</strong> του Λουξεμβούργου η <span class="noitigrammi">ΝΟΗΤΗ ΓΡΑΜΜΗ</span> παρουσίασε τη δεύτερη δίγλωσση παραγωγή της, την διαδραστική performance/εγκατάσταση <strong>I remember | Θυμάμαι</strong>, εμπνευσμένη από το ομώνυμο βιβλίο του <em>Joe Brainard</em>, η οποία παρουσιάστηκε επίσης στο <strong>Face to Face Festival</strong>, που διοργάνωσε το <strong>Lost Theatre</strong> στο Λονδίνο (13/7/2014), τα <strong>49<sup>α</sup> ΔΗΜΗΤΡΙΑ</strong> της Θεσσαλονίκης (4-5/10/2014) καθώς και στο <strong>3<sup>ο</sup> Διεθνές Φεστιβάλ Πειραματικού Θεάτρου του <em>Ανόι</em></strong> (Hanoi, Vietnam, 12–19/11/2016), όπου απέσπασε το <strong>Βραβείο Πρωτοποριακής Παράστασης</strong> (Award for Experimental Innovation).</p>

