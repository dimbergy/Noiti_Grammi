<p>Scottish psychiatrist R. D. Laing gained a reputation in the late 1960&rsquo;s for his radical objection to conventional psychiatry. His early books <strong>The Divided Self</strong> and <strong>The Politics of Experience</strong> questioned the right of society to proclaim itself sane and others mad.</p>
