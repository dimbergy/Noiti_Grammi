    <blockquote>The rented Toyota, driven with such impatient exuberance by the Senator was speeding along the unpaved unnamed road, taking the turns in giddy skidding slides, and then, with no warning, somehow the car had gone off the road and had overturned in black rushing water, listing to its passenger's side, rapidly sinking.</blockquote>
      <blockquote><em>"Am I going to die? Like this?"</em>
    </blockquote>

    <div class="marginbot-40 paddingtop-20">
        <p>A politician from a wealthy family with a long history in politics, a big fortune and a lot of charm, meets, at a party, a young woman who works as a journalist. A graduate in political sciences from a well known university, she is very interested in politics. During the party, after several drinks and a solitary walk on the beach, the politician expresses his interest in the woman. Towards the end of the evening they leave the party together. While driving his car, the politician loses control and the car falls into a dark swamp and sinks into black water. He manages to escape the sinking vehicle, leaving the woman to drown.</p>
        <p>We follow the story through her eyes - an impressionistic jumble of memories and voices from the past intersected by images from the day of her death. These images, stretched in time and constantly repeated, try to give an explanation for the tragic accident that leads to her slow and agonizing death.</p>
        <p>A performance on the corruption of power, the meaning of atonement, as well as on our attitude towards the absurdity of a violent and unjust death.</p>

