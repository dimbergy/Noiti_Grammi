    <blockquote>«We all live under the threat of the atomic bomb - cancer - cancer inducing substances - disease - unemployment - sexual impotency - we live in fear of fear - of black people - of white people - of the police - of income tax - of interest rates - of parking tickets - of the fear of forgetting our lines - of losing money - of making too much money - of losing our hair - of getting fat - of getting ugly - of appearing silly - boring - shy - stupid - of not knowing which loudspeakers to choose - how to fix a car - a bicycle - how to learn the piano - of the fear of failure - of the fear of exposing ourselves - of being late for work - of not having a pension - insurance - of old age - of death - of war - of being injured in a car accident - of the fear of becoming blind - deaf - of not understanding the joke - of taking risks - of swimming - of diving - of jumping off the springboard - of the fear of sickness - of the fear of moving house - of selling - of buying - of the irrational fear of spiders - dark closets - knives - criminals - people - parties - crowds - clever people - of the fear of speaking your mind - of the fear of women - men - the police - stress - it is for all these reasons that this play is dedicated to those who are afraid.»<br />
