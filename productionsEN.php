    <div class="marginbot-30 paddingtop-20">
<p><strong>KVETCH</strong> is a black comedy - a study on the impact of stress on our lives, a study on the anxiety that keeps us awake at night. It is the demon who is thirsty for our blood and is ready to suck our self-confidence. We are surrounded by scores of problems that are not queuing to be solved, but at any given moment they are ready to jump the queue and to scream until they get our attention, regardless of whether what we have been up to has just been ruined. These kvetch are the neglected children that were born out of a faraway anxiety. We might want them to disappear, we might shout at them, confide them to our therapists, or we take pills to kill them at birth but, somehow, these always return in the end. How often is it that when we speak we engage in an inner conversation with ourselves, hoping to be guided or protected? Sometimes, however, this conversation, which takes place in the depths of our minds, is more real than our actual words. If only we could always put these hidden thoughts into words, our communication would be much more profound! We are like icebergs that float through life slowly and rarely, if ever, do they reveal what lies beneath.</p>
