        <p>Ένας πολιτικός, από εύπορη οικογένεια με μεγάλη ιστορία στην πολιτική, με πολλά λεφτά και γοητεία συναντά σε ένα κοσμικό πάρτι μια νέα γυναίκα, που δουλεύει ως δημοσιογράφος σε ένα περιοδικό. Απόφοιτος πολιτικών επιστημών από γνωστό πανεπιστήμιο, η γυναίκα ενδιαφέρεται πολύ για την πολιτική. Στο πάρτι, μετά από αρκετά ποτά και μια μοναχική βόλτα στην παραλία, ο πολιτικός εκδηλώνει το ενδιαφέρον του για τη γυναίκα. Προς το τέλος της βραδιάς αφήνουν το πάρτι και φεύγουν μαζί. Στη διαδρομή ο οδηγός χάνει τον έλεγχο του αυτοκινήτου, το οποίο πέφτει σ' έναν σκοτεινό βάλτο και βυθίζεται στα μαύρα νερά. Ο άντρας καταφέρνει να απεγκλωβιστεί και να γλιτώσει, αφήνοντας όμως την γυναίκα να πνιγεί.</p>

<p>Εμείς παρακολουθούμε την ιστορία μέσα από τα δικά της μάτια -ένα ιμπρεσσιονιστικό συνονθύλευμα αναμνήσεων και φωνών από το παρελθόν της, σε αντιπαράθεση με εικόνες από τη μέρα του θανάτου της. Εικόνες που επιμηκύνονται στον χρόνο, επαναλαμβάνονται κατ' εξακολούθηση και προσπαθούν να δώσουν μιαν εξήγηση για το τραγικό ατύχημα που την οδηγεί αργά και βασανιστικά στον θάνατο.</p>

<p>Η ιστορία ξεκινά τις πρώτες εκείνες στιγμές, ακριβώς μετά το ατύχημα, τις τελευταίες της στιγμές, καθώς συνειδητοποιεί ότι είναι εγκλωβισμένη στο αυτοκίνητο που βουλιάζει στα μαύρα νερά. Η γυναίκα αρνείται να πιστέψει ότι ο άντρας δεν πρόκειται να γυρίσει πίσω να την σώσει -στην πραγματικότητα μόλις χρησιμοποίησε το κορμί της σαν μοχλό για να σωθεί ο ίδιος. Καθώς παλεύει να κρατήσει το κεφάλι της ψηλά, να εισπνεύσει την έσχατη ποσότητα οξυγόνου που φυλακίστηκε μαζί της μέσα στο βυθισμένο αυτοκίνητο, ετερόκλητες εικόνες περνούν μπροστά από τα μάτια της.</p>

