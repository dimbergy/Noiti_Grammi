    <blockquote>Θυμάμαι την πρώτη φορά που είδα τη μαμά μου να κλαίει.</blockquote>
<blockquote>Θυμάμαι που έψαχνα με το χέρι κάτω απ’ το θρανίο για να βρω κολλημένες τσίχλες.</blockquote>
<blockquote>Θυμάμαι εκείνο το τίναγμα που κάνει το κορμί πριν κοιμηθείς. Είναι σαν να πέφτεις στο κενό.</blockquote>
<blockquote>Θυμάμαι ότι το αγαπημένο μου όνειρο ήταν ότι πετούσα.</blockquote>

    <div class="marginbot-30 paddingtop-20">
<p>Μπαίνεις στον χώρο. Βλέπεις στρώματα στο πάτωμα. Ξαπλώνεις. Κλείνεις τα μάτια. Ακούγεται μια φωνή. Αναμνήσεις. Εκατοντάδες αναμνήσεις. Όνειρα. Φαντασιώσεις. Γεγονότα. Μικροπράγματα της καθημερινότητας. Θραύσματα μνήμης. Σκέψεις αποσπασματικές. Ίσως και ασυνάρτητες. «Πρέπει» και «Μη». Ξεχασμένα από καιρό. Γνώριμοι ήχοι. Τραγούδια που έχουν κολλήσει στο μυαλό σου. Μυρωδιές.</p>

<p>Μια εγκατάσταση/διαδραστική performance, εμπνευσμένη από το βιβλίο του Joe Brainard, το οποίο καταγράφει τις αναμνήσεις από τα παιδικά του χρόνια. Πρόκειται για ένα έργο-ποίημα που ενέπνευσε πολλούς καλλιτέχνες, όπως ο Georges Perec και ο ποιητής Kenneth Koch, ο οποίος χρησιμοποίησε τη φράση «Θυμάμαι...» ως διδακτικό εργαλείο, προκειμένου να μυήσει τους μαθητές στις διαδικασίες παραγωγής ποίησης.</p>
