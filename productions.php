<p>O R. D. Laing, σκοτσέζος ψυχίατρος, έγινε γνωστός στα τέλη της δεκαετίας του '60 για τις ριζοσπαστικές του θέσεις ενάντια στην συμβατική ψυχιατρική. Στα πρώτα του βιβλία <strong>The Divided Self (Ο Διχασμένος Εαυτός)</strong> και <strong>The Politics of Experience (Η Πολιτική της Εμπειρίας)</strong> αμφισβήτησε το δικαίωμα της επίσημης κοινωνίας να αυτοπροσδιορίζεται ως ψυχικά υγιής και να ανακηρύσσει κάποιους τρελούς. Τάχτηκε κατά της «παράλογης βίας» που ασκείται στους ασθενείς που υποβάλλονται σε δραστικές θεραπείες, όπως το ηλεκτροσόκ, ενώ ξεσήκωσε κύμα αμφισβήτησης γύρω από το πρόσωπό του όταν δεν δίστασε να δοκιμάσει το διαλογισμό, τα ψυχεδελικά ναρκωτικά που διευρύνουν τη συνείδηση και άλλες μη συμβατικές τεχνικές στην αναζήτηση ενός κοινού θεραπευτικού πλαισίου ανάμεσα στον γιατρό και τον ασθενή.</p>

<p>Εκτός απ' τα καθαρά επιστημονικά του συγγράμματα ο Laing έγραψε και τρία βιβλία που έχουν λογοτεχνικό χαρακτήρα. Τα <strong>Do You Love Me?</strong> και <strong>Knots</strong> είναι «μία ψυχαγωγία σε διάλογο και στίχους» - κείμενα που περιγράφουν τους «κόμπους» (knots) και τα αδιέξοδα των ανθρώπινων σχέσεων. Το <strong>The Facts of Life</strong> αποτελείται από ημερολόγια, αναμνήσεις και σκέψεις, που φανερώνουν την πεποίθηση του Laing ότι οι διαταραγμένες προσωπικότητες είναι υγιείς άνθρωποι που αντιδρούν άλογα σε ένα περιβάλλον συναισθηματικής αστάθειας, ψυχρότητας και βίας. Άλλωστε, ο ίδιος ο Laing ήταν ένα κακοποιημένο από τους γονείς του παιδί κι αυτό σίγουρα αποτελεί έναν οιωνό της κατοπινής του επαγγελματικής εστίασης στον κοινωνικό και διαπροσωπικό κόσμο μέσα στον οποίο μεγαλώνουν οι άνθρωποι.</p>

<p>Το κείμενο της παράστασης βασίζεται στα τρία αυτά βιβλία και αποτελεί μια προσπάθεια διείσδυσης στην τραυματισμένη παιδική ψυχή ενός αγοριού που ασφυκτιά στην προσπάθειά του να κατανοήσει τα παράλογα παιχνίδια που παίζουν οι γονείς. Παιχνίδια στα οποία αναπόφευκτα καταφεύγει και ο ίδιος αφού δεν ξέρει πώς να λειτουργήσει διαφορετικά σε μια συντροφική σχέση.</p>


