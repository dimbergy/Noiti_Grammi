    <blockquote>Όλοι τραγουδάμε.</blockquote>

       <blockquote>Τραγουδάμε πιο συχνά από ό,τι θυμόμαστε.</blockquote>

       <blockquote>Μαζί με τον τραγουδιστή του ραδιοφώνου</blockquote>

       <blockquote>πίσω από το τιμόνι ενός αυτοκινήτου,</blockquote>

       <blockquote>στο ντους,</blockquote>

       <blockquote>κρατώντας μια βούρτσα για μικρόφωνο μπροστά στον καθρέφτη,</blockquote>

       <blockquote>στο δρόμο για το σούπερ μάρκετ τα Σάββατα το πρωί,</blockquote>

       <blockquote>πάνω από τηγάνια και κατσαρόλες,</blockquote>

       <blockquote>στο ασανσέρ φεύγοντας απ’ τη δουλειά.</blockquote>

    <div class="marginind marginbot-30 paddingtop-20">
        <p>Μερικές φορές όμως τραγουδάμε συνειδητά. Όταν κάτι συμβαίνει και το τραγούδι επαναφέρει μνήμες, εικόνες, λόγια. Αυτά που ζήσαμε και είπαμε, είδαμε κι ακούσαμε. Κι όπως τραγουδάμε, είναι σαν να τα κάνουμε όλα αυτά δικά μας πάλι, μόνο πιο συνειδητά, πιο αποφασιστικά. </p>

   <p>Γιατί τα τραγούδια δεν μπορείς να τα ξεγελάσεις. Τις αναμνήσεις τις αλλάζεις όσο περνάει ο χρόνος. Τις διορθώνεις, τις συμπληρώνεις· άλλες, όπως λένε οι ψυχολόγοι, τις επινοείς. Τα τραγούδια όμως έχουν τη δική τους αντοχή. Κι αυτά που φέρνουν μαζί τους, τα φέρνουν γρήγορα, πριν προλάβουμε να τα επεξεργαστούμε και να τα λογοκρίνουμε.  </p>

   <p>Έτσι, μαζί με τα τραγούδια, η παράσταση είναι φτιαγμένη και από όλα εκείνα που τα τραγούδια φέρνουν μαζί τους: στιγμές από την εποχή τους, εικόνες, κουβέντες, όπως τις καταλαβαίνουμε και τις νιώθουμε τώρα: με συγκίνηση, ντροπή, γέλιο ή θυμό. Όχι απλά για να θυμηθούμε το παρελθόν, όχι για να το καταδικάσουμε ή να το ωραιοποιήσουμε, αλλά για να το κερδίσουμε, όλοι μαζί κι ο καθένας από τη μεριά του. Τραγουδώντας το.</p>

