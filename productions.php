    <blockquote>Αν έπρεπε να πετάξεις όλα σου τα ρούχα και να κρατήσεις μόνο ένα, ποιο θα ήταν αυτό;</blockquote>

        <blockquote>Τι θα φορούσες για να ξεχωρίσεις;</blockquote>

        <blockquote>Τι θα ‘βαζες για να περάσεις απαρατήρητη;</blockquote>

        <blockquote>Θα μπορούσες να ζεις σε ένα σπίτι χωρίς καθρέφτη;</blockquote>

        <blockquote>Θα ήθελες να μπορούσες να κυκλοφορείς γυμνή στον δρόμο;</blockquote>

        <blockquote>Έχασες ποτέ το ένα γάντι, το ένα σκουλαρίκι, τη μία κάλτσα;</blockquote>

        <blockquote>Όταν είναι να πετάξεις ένα ρούχο, μην πονάς για τον αποχωρισμό, απλά ευχαρίστησέ το για τη χαρά που σου πρόσφερε και γιόρτασε την νέα του ζωή.</blockquote>

        <blockquote>Ποτέ μα ποτέ, μη δένεις τα καλσόν! Ποτέ, μα ποτέ, μην κάνεις τις κάλτσες σου κουβαράκια.  Ήρθε η ώρα κι αυτές  να ξεκουραστούν... Όλη τη μέρα ήταν στους δρόμους.</blockquote>

        <blockquote>Να τις φυλάξεις όλες τις φωτογραφίες σου που δεν σου αρέσεις. Σε είκοσι χρόνια θα τις βλέπεις και θα λες, «τι όμορφη που ήμουν τότε!».</blockquote>

    <div class="marginind marginbot-30 paddingtop-20">
        <p>Οι ιστορίες που διηγούμαστε στην παράσταση αφορούν τα ρούχα ως οικειότητα, ως συναίσθημα και ως μνήμη.</p>

    <p>Τα ρούχα ως εργαλεία για μεταμόρφωση.</p>

    <p>Ως ένδειξη θριάμβου.</p>

    <p>Ως σημάδι συγγένειας και επαφής.</p>

<p>Είναι σαν να μιλούν 328 γυναίκες… Γιατί το υλικό μας ήταν πλούσιο και ετερογενές: μοιράσαμε ερωτηματολόγια, πήραμε συνεντεύξεις, διαβάσαμε μελέτες και ποιήματα, κρυφακούσαμε συνομιλίες, αναζητήσαμε αναρτήσεις στα μέσα κοινωνικής δικτύωσης, ξεφυλλίσαμε περιοδικά, ακούσαμε τραγούδια, ξετρυπώσαμε παλιές  φωτογραφίες και… και… και…</p>

<p>Η σχέση ειδικά των γυναικών με το ντύσιμο είναι μια σχέση εξαιρετικά λεπτή, πολυσήμαντη και συχνά αποκαλυπτική - μια σχέση με πολλές πτυχές: άλλοτε φανερές και ερμηνεύσιμες, άλλοτε σκοτεινές και δυσερμήνευτες.</p>

<p>Είναι αδιαμφισβήτητο γεγονός το ότι οι ενδυματολογικές μας επιλογές μας καθορίζουν. Μας προσδίδουν ή μη κύρος, αντικατοπτρίζουν την ιδεολογία μας, προσδιορίζουν τον επαγγελματικό χώρο στον οποίο ανήκουμε, εκφράζουν τη σεξουαλικότητά μας, απηχούν την ηθική μας, μας εντάσσουν σε ομάδες ή μας διαφοροποιούν... Τα κάνουμε εργαλείο, πανοπλία, μάσκα μας.</p>

<p>Στην παράστασή μας κάποια στιγμή, αφήσαμε τα ρούχα στην άκρη και αφουγκραστήκαμε. Γιατί τα ρούχα λένε ιστορίες.</p>
