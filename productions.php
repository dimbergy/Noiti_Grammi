     <div class="marginbot-40 paddingtop-20">
        <p>Η παράσταση <strong>Όλα όσα έχω ξεχάσει μπορούν να γεμίσουν ένα σπίτι;</strong> είναι ένας καταιγισμός από ερωτήσεις που μιλούν για την ανθρώπινη κατάσταση. Ερωτήσεις που, αναπόφευκτα, όλοι θέτουμε στον εαυτό μας κάποια στιγμή στη ζωή μας. «Μεγάλες» και «μικρές» ερωτήσεις, φιλοσοφικές και μη. Ερωτήσεις που μιλούν για την αγάπη, το φόβο, την μοναξιά, την ανάγκη να ανήκεις κάπου, τη δίψα για εξουσία, ερωτήσεις που κρύβουν την εκμετάλλευση του ανθρώπου από τον άνθρωπο.</p>

<p>Αφορμή και έμπνευσή μας μια εικαστική εγκατάσταση των Peter Fischli και David Weiss με τίτλο <strong>Questions</strong>. Και μετά, θαρρείς απολύτως φυσικά, άρχισαν να έρχονται στον δρόμο μας κι άλλα κείμενα που είχαν το ίδιο θέμα. Έτσι συναντηθήκαμε με τον John Cage, τον Ben Vautier και τους Forced Entertainment. Και νοιώσαμε ότι όλους μας συνδέει κάτι: η ανάγκη μας να ρωτάμε και να ζητάμε απαντήσεις:</p>

<p>► Μήπως η ευτυχία με ψάχνει σε λάθος μέρος;<br />
► Να πάω σε μιαν άλλη πόλη και να νοικιάσω ένα διαμέρισμα με ψεύτικο όνομα;<br />
► Να αφαιρέσω την εξάτμισή μου και να αρχίσω να οδηγώ στη γειτονιά τη νύχτα;<br />
► Είναι το σώμα μου ξενοδοχείο;<br />
► Είναι τα ψέματά μου ένα αριστούργημα πρωτοπορίας και εφευρετικότητας;</p>

<p>Στην διαδικασία των προβών καλωσορίσαμε στην αίθουσα κάθε τι προσωπικό και απόρρητο. Ήρθαν ερωτήσεις με τις οποίες γελάσαμε και άλλες με τις οποίες κλάψαμε. Θέσαμε ερωτήσεις στις οποίες δεν μπόρεσε ν' απαντήσει κανείς και ερωτήσεις που γνωρίζαμε πολύ καλά ότι έκρυβαν κάτι άλλο από το προφανές. Παρατηρήσαμε τις ερωτήσεις να τραγουδούν και να χορεύουν, να πέφτουν και να σηκώνονται. Κλείσαμε το στόμα ο ένας του άλλου σε ερωτήσεις που δεν αντέχαμε ν' ακούσουμε. Καθίσαμε στην σιωπή και στο σκοτάδι και προσπαθήσαμε να ανασύρουμε τις ερωτήσεις που ξεφυτρώνουν από το πουθενά λίγο πριν μας πάρει ο ύπνος. Αφήσαμε αυτές τις ερωτήσεις να πούνε ιστορίες... Κι όλα αυτά με μουσική και χιούμορ...</p>
