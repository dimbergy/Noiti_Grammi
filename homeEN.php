          <p>Over the last few years we have been actively engaged in producing devised and performance pieces, trying to develop our own individual method – a method which mainly aims at creating images rather than words. We have been moving away from traditional theatre, maintaining that the clear distinction between the audience and the actor hinders the growth of the feeling that we all belong to a community and we are all troubled by the same worries. <p>


             <p>We do not wish to keep the spectator in the dark, we do not expect him to totally forget himself and immerse in the world of fiction. We do not want the spectator to lose sense of time and place. On stage, the actor plays a role, or multiple ones, without losing his identity. At the same time, the spectator, through his emotional participation, remains fully conscious of his being actively in communion with the event.</p>

